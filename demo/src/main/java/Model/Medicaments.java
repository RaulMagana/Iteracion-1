package demo.src.main.java.Model;

public class Medicaments {
    private int id;
    private String name;

    public Medicaments(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return name;
    }

    public void setNombre(String name) {
        this.name = name;
    }


    
}
